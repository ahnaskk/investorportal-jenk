<template>
  <div class="wrapper">
    <tableSection
      v-if="data && !loading && !error && calculatedData"
      :data="calculatedData"
      :columns="dataColumn"
      :rangeSelector="true"
      :showSlot="false"
      :rangeFilters="[
        {
          text: 10,
          value: 10,
        },
        {
          text: 20,
          value: 20,
        },
        {
          text: 30,
          value: 30,
        },
      ]"
      :filterRange="request.per_page"
      :filterFrom="request.sDate"
      :filterTo="request.eDate"
      :filterMerchant="request.merchant_name"
      :searchKeyword="request.filter"
      :hideSearch="false"
      :pagination="data.meta"
    />
    <preloader v-else></preloader>
  </div>
</template>

<script>
import preloader from "@ac/preloader"
import tableSection from "@ac/tableSection.vue";
import { mapState } from "vuex";
export default {
  name: "total-portfolio-earningss",
  components: {
    tableSection,
    preloader
  },
 data() {
    return {
      error: false,
      errorMsg: null,
    };
  },
   computed: {
     ...mapState('api', {
      data: s => s.data.portfolioEarning,
      loading: (s) => s.loading.portfolioEarning,
      dataColumn: s => s.data.dataColumn,
      dataColumnLoading : s => s.loading.dataColumn,
    }),
    calculatedData(){
      let data = []
      let tr = {}
      if( !this.loading && !this.dataColumnLoading && this.data.data && this.dataColumn.data ){
        if (this.data.data.length < 1) return []
        this.data.data.forEach(td => {
          tr = {}
          this.dataColumn.data.forEach(th =>{
            if ( td.hasOwnProperty(th.data) ){
              tr[th.title] = td[th.data]
            }
          })
          data.push(tr)
        })
      }
      // sum row
      if(!this.dataColumnLoading && this.dataColumn.data){
        let footer = {}
        this.dataColumn.data.forEach(th =>{
          footer[th.title] = ""
        })
        console.log
        footer["Bills"] = this.data.total_bills
        footer["Credited Amount"] = this.data.total_credit_amount
        footer["Distributions"] = this.data.total_distributions
        footer["Distributions"] = this.data.total_portfolio
        footer["Portfolio Value"] = this.data.total_distributions
        footer["Total Portfolio Earnings"] = this.data.total_portfolio_earning
        data.push(footer)
      }
      return data
    },
    request(){
      let searchable = []
      if(!this.dataColumnLoading && this.dataColumn && this.dataColumn.data){
        searchable = this.dataColumn.data.reduce ( (searchables , field ) => {
          if (field.searchable){
            searchables.push(field.data)
          }
          return searchables
        },[])
      }
      return {
        per_page: 10,
        filter: "",
        page: 1,
        searchable
      }
    }
  },
  methods:{
    applyFilters(data){
      console.log('submit',data)
    }
  },
  created() {
    this.$api({
      url: "/report/portfolio-earning",
      field:'portfolioEarning',
      post:this.request,
      force: true,
      catchErr: (d) => !d
    })
    .then(d=>{
      console.log('resolved',d);
    })
    .catch((e) => console.log("Catched on component", e));
    //Table Headers
    if ( !this.dataColumn || !this.dataColumn.data ){
      this.$api({
        url: "/report/portfolio-earning-columns",
        field:'dataColumn',
        force: true,
        catchErr: (d) => !d
      })
      .then(d=>{
        console.log('resolved',d);
      })
      .catch((e) => console.log("Catched on component", e));
    }
  },
};
</script>

